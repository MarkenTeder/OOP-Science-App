    private void writeObjectToFile(){
        try{
            File f = new File("data.txt");
            FileOutputStream fow = new FileOutputStream(f);
            ObjectOutputStream oos = new ObjectOutputStream(fow);
            
            oos.writeObject(aList);
            
            fow.close();
            oos.close();
        }
        catch(Exception ex){
            System.out.println(ex.getMessage());
        }
    }
    private void readObjectFromFile(){
        try{
            File f = new File("data.txt");
            FileInputStream fis = new FileInputStream(f);
            ObjectInputStream ois = new ObjectInputStream(fis);
            
            ArrayList<StudyPlan> arr = new ArrayList<>();
            
            arr = (ArrayList<StudyPlan>) ois.readObject();
            
            aList = arr;
            ois.close();
            fis.close();            
        }
        catch(Exception ex){
            System.out.println(ex.getMessage());
        }
    }

