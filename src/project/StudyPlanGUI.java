    private void btnDisplayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDisplayActionPerformed
        // TODO add your handling code here:
        readObjectFromFile();
         for (int i = 0; i < aList.size(); i++) {
            if (aList.size() >= 0) {
                JOptionPane.showMessageDialog(null, "Subject Name: " + aList.get(i).getName() + "\n Day of Study: " + aList.get(i).getDay() + "\n Time to Start: " + aList.get(i).getStarttime() + "\n Time to Finish: " + aList.get(i).getFintime());
            } else {
                JOptionPane.showMessageDialog(null, "No entries found");
            }
        }
             
         
    }//GEN-LAST:event_btnDisplayActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String SearchType = JOptionPane.showInputDialog(null,"Enter Day: ");
        
        for(int i = 0; i < aList.size();i++){
            if(SearchType.equalsIgnoreCase(aList.get(i).getDay())){
                 JOptionPane.showMessageDialog(null, "Subject Name: "+aList.get(i).getName()+"\n Day of Study: "+aList.get(i).getDay()+"\n Time to Start: "+aList.get(i).getStarttime()+"\n Time to Finish: "+aList.get(i).getFintime());
            }
            if(!SearchType.equalsIgnoreCase(aList.get(i).getDay())){
                JOptionPane.showMessageDialog(null,"Entry not found");
            }
        }
            
    }//GEN-LAST:event_btnSearchActionPerformed

    
    
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        name = Nametf.getText();
        day = Studytf.getText();
        starttime = Integer.parseInt(Starttf.getText());
        fintime = Integer.parseInt(Finishtf.getText());
        
        //create a new Assessment object
        StudyPlan a = new StudyPlan();
        
        //add contents of textfields to q
        a.setName(name);
        a.setDay(day);
        a.setStarttime(starttime);
        a.setFintime(fintime);
        
        //add q to array of Assessment objects
        aList.add(a);
        //increase count to show that another object has been added to the array
        count++;
        
        Nametf.setText("");
        Studytf.setText("");
        Starttf.setText("");
        Finishtf.setText("");
        
        writeObjectToFile();
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String question = JOptionPane.showInputDialog(null, "Delete File?");

        File f = new File("data.txt");

        if (f.exists()) {
            f.delete();
            JOptionPane.showMessageDialog(null, "File Gone");
        }

        for (int i = 0; i < aList.size(); i++) {
            aList.remove(i);
        }
    

    }//GEN-LAST:event_btnDeleteActionPerformed

